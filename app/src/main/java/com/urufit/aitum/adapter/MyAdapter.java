package com.urufit.aitum.adapter;

import android.content.Context;

import androidx.fragment.app.Fragment;
import androidx.fragment.app.FragmentManager;
import androidx.fragment.app.FragmentPagerAdapter;

import com.urufit.aitum.fragment.PlayerProfileFragment;
import com.urufit.aitum.fragment.TeamProfileFragment;

public class MyAdapter extends FragmentPagerAdapter {

    private Context myContext;
    int totalTabs;

    public MyAdapter(Context context, FragmentManager fm, int totalTabs) {
        super(fm);
        myContext = context;
        this.totalTabs = totalTabs;
    }

    // this is for fragment tabs
    @Override
    public Fragment getItem(int position) {
        switch (position) {
            case 0:
                TeamProfileFragment teamProfileFragment = new TeamProfileFragment();
                return teamProfileFragment;
            case 1:
                PlayerProfileFragment playerProfileFragment = new PlayerProfileFragment();
                return playerProfileFragment;

            default:
                return null;
        }
    }
    // this counts total number of tabs
    @Override
    public int getCount() {
        return totalTabs;
    }
}